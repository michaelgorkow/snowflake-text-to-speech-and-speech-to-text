    RUNTIME_NAME = 'SYSTEM$GPU_RUNTIME' 
    COMPUTE_POOL = 'SYSTEM_COMPUTE_POOL_GPU'
    EXTERNAL_ACCESS_INTEGRATIONS = ( 'ai_external_access_integration' )
    MAIN_FILE = 'SPEECH_TO_TEXT_NOTEBOOK.ipynb';
